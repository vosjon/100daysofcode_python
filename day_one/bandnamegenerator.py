# This is the Band Name Generator program.
# Day 1 of the 100 Days of Code Course

print("Welcome to the Band Name Generator!")

name = input("Yo dude, enter your name: ")

city = input("What city did you grow up in? ")

pet_name = input("What is your pet's name? ")

band_name = city + " " + pet_name

print(f"{name}, your gnarly band name is: {band_name}!")
